public class Elever {
    String namn;
    Long personNumer;
    String email;
    Long mobilNumer;
    String klass;
    String program;
    
    public Elever(String namn, Long personNumer, String email, Long mobilNumer, String klass, String program){
        this.namn = namn;
        this.personNumer = personNumer;
        this.email = email;
        this.mobilNumer = mobilNumer;
        this.klass = klass;
        this.program = program;
    }
}
